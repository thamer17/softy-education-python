

def calcul(n_1, n_2):
    print(n_1," + ", n_2, " = ", n_1+n_2)
    print(n_1," - ", n_2, " = ", n_1-n_2)
    print(n_1," * ", n_2, " = ", n_1*n_2)
    print(n_1," / ", n_2, " = ", n_1/n_2)
    print(n_1," % ", n_2, " = ", n_1%n_2)

n_1 = int(input("Enter a first number: "))
n_2 = int(input("Enter a second number: "))
calcul(n_1, n_2)