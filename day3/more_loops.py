animals = ["cat", "dog", "hamster"]

# for 
for animal in animals:
    #instruction
    print("We love ", animal)

# while
age = 1
while age <= 18:
    if age < 18:
        print("I'm still child")
    else:
        print("I'm old")
    age += 1

